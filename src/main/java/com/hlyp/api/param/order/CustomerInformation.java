package com.hlyp.api.param.order;

public class CustomerInformation {
    private String gender;//性别
    private String name;//姓名
    private String height;//身高
    private String weight;//体重
    private String heightunited;//身高单位
    private String weightunited;//体重单位
    private String email;//邮箱
    private String address;//地址
    private String tel;//电话
    private String memos;//顾客备注

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getHeight() {
        return height;
    }

    public void setHeight(String height) {
        this.height = height;
    }

    public String getWeight() {
        return weight;
    }

    public void setWeight(String weight) {
        this.weight = weight;
    }

    public String getHeightunited() {
        return heightunited;
    }

    public void setHeightunited(String heightunited) {
        this.heightunited = heightunited;
    }

    public String getWeightunited() {
        return weightunited;
    }

    public void setWeightunited(String weightunited) {
        this.weightunited = weightunited;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getTel() {
        return tel;
    }

    public void setTel(String tel) {
        this.tel = tel;
    }

    public String getMemos() {
        return memos;
    }

    public void setMemos(String memos) {
        this.memos = memos;
    }
}
