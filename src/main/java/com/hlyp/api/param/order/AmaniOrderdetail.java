public class AmaniOrderdetail {

    private String sizespecheight;
    private String categoryid;
    private String bodystyle;
    private String partsize;
    private String ordersprocess;

    public String getSizespecheight() {
        return sizespecheight;
    }

    public void setSizespecheight(String sizespecheight) {
        this.sizespecheight = sizespecheight;
    }

    public String getCategoryid() {
        return categoryid;
    }

    public void setCategoryid(String categoryid) {
        this.categoryid = categoryid;
    }

    public String getBodystyle() {
        return bodystyle;
    }

    public void setBodystyle(String bodystyle) {
        this.bodystyle = bodystyle;
    }

    public String getPartsize() {
        return partsize;
    }

    public void setPartsize(String partsize) {
        this.partsize = partsize;
    }

    public String getOrdersprocess() {
        return ordersprocess;
    }

    public void setOrdersprocess(String ordersprocess) {
        this.ordersprocess = ordersprocess;
    }
}
